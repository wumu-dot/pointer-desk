/**
 * a4988.h — A4988 步进电机驱动接口
 *
 * 控制 42步进电机 (NEMA17) 的位置和运动。
 * TIM2 CH1 PWM 产生 STEP 脉冲，硬件自动输出，CPU 不占。
 */

#ifndef A4988_H
#define A4988_H

#include <stdint.h>
#include <stdbool.h>

/* ================================================================
 * 微步细分模式
 * ================================================================ */
typedef enum {
    STEP_FULL       = 0,  /* 200 步/圈 */
    STEP_HALF       = 1,  /* 400 步/圈 */
    STEP_QUARTER    = 2,  /* 800 步/圈 */
    STEP_EIGHTH     = 3,  /* 1600 步/圈 */
    STEP_SIXTEENTH  = 4,  /* 3200 步/圈 */
} microstep_t;

/* ================================================================
 * 位置 & 运动参数
 * ================================================================ */
typedef struct {
    int32_t  target_steps;     /* 目标位置 (微步) */
    int32_t  current_steps;    /* 当前位置 (微步) */
    uint32_t max_speed;        /* 最大速度 (微步/秒) */
    uint32_t acceleration;     /* 加速度 (微步/秒²) */
    bool     is_moving;        /* 运动中标志 */
    bool     direction;        /* 当前方向 */
} motor_state_t;

/* ================================================================
 * 初始化 & 控制
 * ================================================================ */
void a4988_init(void);
void a4988_set_microstep(microstep_t mode);
void a4988_enable(bool en);         /* true=使能, false=禁用 (释放电机) */
void a4988_set_direction(bool dir); /* true=正转(顺时针), false=反转 */

/* ================================================================
 * 运动控制
 * ================================================================ */
/* 设置脉冲频率 (Hz), 电机任务调用 */
void a4988_set_speed(uint32_t steps_per_sec);

/* 发送单个脉冲 (用于微步调试) */
void a4988_pulse_once(void);

/* 紧急停止 */
void a4988_emergency_stop(void);

/* ================================================================
 * 状态查询
 * ================================================================ */
motor_state_t a4988_get_state(void);
bool a4988_is_moving(void);

/* ================================================================
 * 振动效果 (计时结束提示)
 * ================================================================ */
void a4988_vibrate(uint8_t count, uint16_t interval_ms);

#endif /* A4988_H */
